function somar(a,b){
    return a+b
}
console.log(`Somar: ${somar(10,10)}`)

function subtrair(a,b){
    return a-b
}
console.log(`Subtrair: ${subtrair(10,10)}`)

function multiplicar(a,b){
    return a*b
}
console.log(`Multiplicar: ${multiplicar(10,10)}`)

function dividir(a,b){
    return a/b
}
console.log(`Dividir: ${dividir(10,10)}`)